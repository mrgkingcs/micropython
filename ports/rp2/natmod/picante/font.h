#include "py/dynruntime.h"

//======================================================================================================
// FontInfo registry
//======================================================================================================
#define MAX_FONTS (4)

typedef struct _FontInfo {
    uint8_t charPxW;
    uint8_t charPxH;
    uint8_t advanceX;
    uint8_t advanceY;   // i.e. line-height
    uint8_t firstCharCode;
    uint8_t finalCharCode;
    uint8_t* buffer;
} FontInfo;


//======================================================================================================
// Reset font registry
//======================================================================================================
void resetFonts();

//======================================================================================================
// Allocate a font slot (0xff on fail)
//======================================================================================================
uint8_t allocFont();

//======================================================================================================
// return the number of currently allocated fonts
//======================================================================================================
uint8_t getNumFonts();

//======================================================================================================
// get the FontInfo struct for an allocated font slot
//======================================================================================================
FontInfo* getFont(uint8_t fontID);
